+ (NSString *)reverseString:(NSString *)strSrc
{
    NSMutableString* reverseString = [[NSMutableString alloc] init];
    NSInteger charIndex = [strSrc length];
    while (charIndex > 0) {
        charIndex --;
        NSRange subStrRange = NSMakeRange(charIndex, 1);
        [reverseString appendString:[strSrc substringWithRange:subStrRange]];
    }
    return reverseString;
}
///是否是纯数字
- (BOOL)isPureNumber{
    if (self.length == 0) {
        return NO;
    }
    NSString *regex = @"[0-9]*";
    NSPredicate *pred = [NSPredicate predicateWithFormat:@"SELF MATCHES %@",regex];
    if ([pred evaluateWithObject:self]) {
        return YES;
    }
    return NO;
}
///是否是纯英文
- (BOOL)isPureEnglish{
    NSString *regex =@"[a-zA-Z]*";
    NSPredicate *pred = [NSPredicate predicateWithFormat:@"SELF MATCHES %@",regex];
    if (![pred evaluateWithObject:self]) {
        return NO;
    }
    return YES;
}

/** 是否纯特殊字符*/
-(BOOL)isAllCharacterString{
    NSString *regex = @"[~`!@#$%^&*()_+-=[]|{};':\",./<>?]{,}/";
    NSInteger str_length = [self length];
    NSInteger allIndex = 0;
    for (int i = 0; i<str_length; i++) {
        NSString *subStr = [self substringWithRange:NSMakeRange(i, 1)];
        if([regex rangeOfString:subStr].location != NSNotFound) {
            allIndex++;
        }
    }
    if (str_length == allIndex) {
        return YES;
        
    } else {
        return NO;
    }
}
